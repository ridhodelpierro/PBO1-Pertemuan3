public class PercabanganSwitch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("Urang Banjar");
                break;
            case 2:
                System.out.println("Urang Dayak");
                break;
            case 3:
                System.out.println("Urang PaHuluan");
                break;
            case 4:
                System.out.println("Urang Pasisir");
                break;
            default:
                System.out.println("Suku Penghuni Kalsel");
                break;

        }
    }
}
